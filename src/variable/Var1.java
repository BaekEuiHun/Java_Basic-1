package variable;

public class Var1 {
    public static void main(String[] args) {

        int a; //변수 선언
        a = 1; //변수 초기화
        System.out.println("a = " + a);

        int b = 2;
        System.out.println("b = " + b);

        int c = 3, d = 4;
        System.out.println("c = " + c);
        System.out.println("c = " + d);
    }
}
